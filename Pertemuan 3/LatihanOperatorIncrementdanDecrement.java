public class LatihanOperatorIncrementdanDecrement{
    public static void main(String[] args){
        int i = 198;
        //increment
        System.out.println("i :" + i);
        System.out.println("++i :" + ++i);
        System.out.println("i++:" + i++);
        //decrement
        System.out.println("--i :" + i);
        System.out.println("i-- :" + i--);
        System.out.println("i :" + i);
    }
}